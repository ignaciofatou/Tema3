    private void jBMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMasActionPerformed
        
        numPartidos = Integer.valueOf(jTFNumPartidos.getText());

        if (numPartidos < MAX_PARTIDOS) {
            numPartidos++;
            jTFNumPartidos.setText(""+numPartidos);
        }
        //Activa o Desactiva los Botones Mas o Menos
        validaBotones();
    }//GEN-LAST:event_jBMasActionPerformed

    private void jBMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMenosActionPerformed

        numPartidos = Integer.valueOf(jTFNumPartidos.getText());

        if (numPartidos > MIN_PARTIDOS) {
            numPartidos--;
            jTFNumPartidos.setText(""+numPartidos);
        }
        //Activa o Desactiva los Botones Mas o Menos
        validaBotones();
    }//GEN-LAST:event_jBMenosActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        inicializaTextoResultados();
        generaVotos();
        imprimeResultados();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void inicializaTextoResultados(){
        jTAResultado.setText("RESULTADO DE LAS ELECCIONES\n");
        jTAResultado.append("--------------------------------------------------\n");
    }
    
    private void validaBotones(){
        if (numPartidos >= MAX_PARTIDOS) {
            jBMas.setEnabled(false);
        } else {
            jBMas.setEnabled(true);
        }

        if (numPartidos <= MIN_PARTIDOS) {
            jBMenos.setEnabled(false);
        } else {
            jBMenos.setEnabled(true);
        }
    }
    
    private void inicializaVentana(){
        jTFNumPartidos.setText(""+MIN_PARTIDOS);
        jBMenos.setEnabled(false);
        jBMas.setEnabled(true);
        jTFNumPartidos.setEditable(false);
        jTFNumPartidos.setEnabled(true);
    }
    
    private void generaVotos(){
        
        Random generadorNum = new Random();
        matrizPartidos = new int[numPartidos];
        int votoAleatorio;
        boolean ganador = false;
        
        do{
            votoAleatorio = generadorNum.nextInt(numPartidos);
            matrizPartidos[votoAleatorio]= matrizPartidos[votoAleatorio] + 1;
            
            //Si el Parido que ha sido votado tiene 1000 votos
            if (matrizPartidos[votoAleatorio] == MAX_VOTOS)
                ganador = true;            
        } while (!ganador);
        
        //Mensaje con el Ganador de las Elecciones
        jTAResultado.append("EL PARTIDO GANADOR ES EL "+(votoAleatorio + 1)+"\n");
    }
    
    private void imprimeResultados(){
        
        //Ponemos una Linea Separadora
        jTAResultado.append("--------------------------------------------------\n");
        
        //Imprimimos los Resultados
        for (int x=0; x<numPartidos;x++){
            jTAResultado.append("El Partido: "+(x+1)+" ha obtenido: "+ matrizPartidos[x] + " votos\n");
        }
    }
    
