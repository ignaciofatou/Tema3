    private void jBCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCalcularActionPerformed
        
        int numero = Integer.valueOf(jTFNumero.getText());

        int numActual = 1;
        long resultado = numActual;
        jTAResultado.setText(""+ numero +"! =");        
        
        for (numActual=1; numActual < numero; numActual++) {
            resultado = resultado * numActual;
            jTAResultado.append(" " + numActual + " x");
        }
        resultado = resultado * numActual;
        
        //Si el Resultado del Factorial no supera el Maximo permitido 
        //por el tipo Long
        if (numero <= 25)
            jTAResultado.append(" " + numActual + " = " + resultado);
        else
            jTAResultado.append(" " + numActual + " = ERROR - VALOR DEMASIADO GRANDE");
    }//GEN-LAST:event_jBCalcularActionPerformed

